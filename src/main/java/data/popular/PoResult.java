package data.popular;


// 인기순 영화리스트
public class PoResult {
	
	String id;  // 영화 아이디
	String posterPath;   // 포스터
	String overview;   // 줄거리
	String title;  // 제목
	
	
	public String getId() {
		return id;
	}
	public String getPosterPath() {
		return posterPath;
	}
	public String getOverview() {
		return overview;
	}
	public String getTitle() {
		return title;
	}
	
}
